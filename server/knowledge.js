// Компактный текст базы знаний для системного промпта LLM
export function knowledgeText() {
  const models = MODELS.map(m => `- ${m.name} (${m.topic}): ${m.principle} → ${m.url}`).join('\n');
  const faq = FAQ.map(f => `• ${f.a}`).join('\n');
  return [
    `Проект: ${SITE.name}. ${SITE.about}`,
    `Пилотные школы: ${SITE.schools.join(', ')}.`,
    '',
    'Модели:',
    models,
    '',
    'Факты и частые ответы:',
    faq,
  ].join('\n');
}
